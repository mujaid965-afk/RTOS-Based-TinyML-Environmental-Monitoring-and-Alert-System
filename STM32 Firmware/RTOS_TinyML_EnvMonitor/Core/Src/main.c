/* USER CODE BEGIN 0 */
void I2C_Scan(void)
{
    char msg[64];

    HAL_UART_Transmit(&huart2,
                      (uint8_t *)"\r\nScanning I2C Bus...\r\n",
                      23,
                      HAL_MAX_DELAY);

    for(uint8_t addr = 1; addr < 128; addr++)
    {
        if(HAL_I2C_IsDeviceReady(&hi2c1,
                                 addr << 1,
                                 3,
                                 100) == HAL_OK)
        {
            sprintf(msg,
                    "Found device at 0x%02X\r\n",
                    addr);

            HAL_UART_Transmit(&huart2,
                              (uint8_t *)msg,
                              strlen(msg),
                              HAL_MAX_DELAY);
        }
    }

    HAL_UART_Transmit(&huart2,
                      (uint8_t *)"Scan Complete\r\n",
                      15,
                      HAL_MAX_DELAY);
}
